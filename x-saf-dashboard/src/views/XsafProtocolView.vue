<p class="m-3"><b>Introduction of X-SAF certification</b></p>
<p class="m-3">X-SAF - What is it, aim to achieve and it’s potential impact
The certification of X-SAF marks a pivotal advancement in the decarbonization of the aviation sector. X-SAF certification comprehensively addresses all CO2 emissions across the entire lifecycle, including both upstream and downstream processes associated with fossil-based kerosene. This holistic approach ensures that all emissions are offset using a blend of certified carbon removal credits, thereby achieving carbon neutrality for the aviation industry. 
</p>
<p class="m-3">
X-SAF not only marks a crucial advancement in reducing aviation-related carbon emissions in the near term but also provides a strategic pathway to incorporate both current and future CDR capacities into the aviation sector. This integration offers a credible solution to address potential shortfalls in physical SAF availability, ensuring that the industry can meet its sustainability targets even as SAF scales towards full economic viability. The certification process mandates rigorous and precise tracking of CO2 emissions, encompassing capture, transportation, transformation, and final storage based on scientific literature. This comprehensive tracking ensures transparency and accountability, reinforcing the aviation industry's commitment to sustainable practices and climate change mitigation.
</p>

<p class="m-3"><b>Principles of X-SAF certification</b></p>
<p class="m-3">
The utilization of fossil-based aviation fuels is characterized by substantial greenhouse gas (GHG) emissions throughout its lifecycle, encompassing both CO2 and non-CO2 GHGs such as methane (CH4), nitrous oxide (N2O), and particulate matter. These emissions manifest at multiple stages—from the initial extraction and refining processes to the final combustion within aircraft engines—each contributing to the cumulative atmospheric burden.
</p>
<p class="m-3">
The lifecycle of fossil-derived aviation fuel initiates with the extraction of crude oil, followed by its refinement into kerosene. This refining process entails several stages including distillation, hydrotreating, and cracking, each of which contributes to the release of CO2 and other GHGs. Upon combustion, the carbon that has been sequestered for millions of years is emitted back into the atmosphere as CO2, in addition to other pollutants such as nitrogen oxides (NOx) and sulfur oxides (SOx).
</p>
<p class="m-3">
To address these emissions comprehensively, a diverse and robust portfolio of certified carbon removal credits is employed. This portfolio includes a range of methodologies such as Biochar, Enhanced Rock Weathering, and Direct Air Capture. Each method is rigorously verified to ensure the permanent removal of CO2 from the atmosphere. These credits are then utilized to offset the emissions associated with fossil-based aviation fuel, effectively neutralizing its carbon footprint and mitigating its impact on climate change.
</p>
<p class="m-3">
The certification process demands meticulous tracking of all GHG emissions throughout the aviation fuel lifecycle, including those associated with crude oil extraction, refining, and combustion, as well as the emissions from transportation involved in carbon sequestration. This holistic approach to carbon accounting encompasses emissions of CH4, N2O, NOx, and SOx, ensuring a comprehensive strategy to mitigate the climate impacts of fossil-based aviation fuels.
</p>
<p class="m-3">
To ensure the long-term efficacy and integrity of this carbon removing strategy, continuous monitoring and verification are essential. This includes tracking the lifecycle emissions of aviation fuel and the permanence of the carbon sequestration activities. Only by maintaining stringent standards can the aviation industry confidently contribute to global climate goals while continuing to meet its operational demands.
</p>

<p class="m-3"><b>Definition and calculation</b></p>
<p class="m-3">Basis for Calculation
The basis for the calculation is the current market price of the minimum selling price of fossil-based kerosene. This price will be adjusted to account for the total downstream and upstream emissions, providing a comprehensive life cycle assessment (LCA) of the fuel.
</p>

<p class="m-3"><b><i>Life Cycle Assessment (LCA) Methodology</i></b></p>
<p  class="m-3"><b><i>1) Data Collection and Emissions Calculation:</i></b>
<p class="m-3">
The total greenhouse gas (GHG) emissions associated with fossil-based kerosene are calculated using sophisticated LCA methodologies. These methodologies rely on scientific literature and industry standards to ensure accuracy and reliability.
</p>
<p class="m-3">
<b><i>The LCA covers all stages of the fuel's lifecycle, including:</i></b>
</p>
<li class="ml-3">Upstream Emissions: Emissions from crude oil extraction, transportation, refining, and distribution.</li>
<li class="ml-3">Downstream Emissions: Emissions from fuel combustion in aircraft engines and any associated processes.</li>
<li class="ml-3">Emissions are expressed in kilograms of CO2-equivalent per liter (kg CO2-eq/L), accounting for CO2, methane (CH4), and nitrous oxide (N2O) emissions, with conversion factors of 86 and 300, respectively, for their 20-year global warming potentials.</li>
</p>

<p class="m-3"><b><i>2) Emission Factors and References:</i></b>
<p class="ml-3">
The emission factors are derived from comprehensive sources, including:
</p>
<li class="ml-3">International Panel on Climate Change (IPCC) Reports</li>
<li class="ml-3">Peer-reviewed Journals and Industry Reports</li>
<p class="ml-3">These sources provide validated data for the different stages of the kerosene lifecycle, ensuring a robust and scientifically sound LCA.
</p></p>

<p class="m-3"><b><i>Portfolio of Certified Carbon Credits</i></b></p>
<p class="m-3">
<b><i>1) Selection of Carbon Credits:</i></b>
<p class="m-3">A portfolio of certified carbon credit options is chosen based on their efficacy in sequestering or removing CO2 emissions. These options include:
</p>
<li class="ml-3">Direct Air Capture (DAC) Technologies</li>
<li class="ml-3">Biochar Production and Application</li>
<li class="ml-3">Enhanced Rock Weathering (ERW)</li>
<p class="m-3">Each carbon credit option is verified and certified by recognized standards such as the Verified Carbon Standard (VCS) or the Gold Standard.
</p></p>

<p class="m-3"><b><i>2) Average Price of Carbon Credits:</i></b></p>
<p class="m-3">
The average price of carbon credits is determined by analyzing the market prices for the selected portfolio. This includes:
</p>
<li class="ml-3">Historical market data and trends</li>
<li class="ml-3">Price variations based on project type and geographic location</li>
<p class="m-3">
The price is calculated on a per-tonne CO2-eq basis, ensuring that the offsets are economically viable.
</p>

<p class="ml-3"><b><i>Calculation of Total Kerosene Price with Carbon removing</i></b></p>
<b class="ml-3"><i>Carbon Offset Cost Calculation:</i></b>
<p class="m-3">
The total emissions in kg CO2-eq/L are multiplied by the average price of carbon credits (per tonne CO2-eq) to determine the carbon offset cost per liter of kerosene. This cost is added to the current market price of the minimum selling price of fossil-based kerosene to obtain the adjusted price.
</p>

<p class="ml-3"><b>The role of upstream and downstream emissions</b></p>
<p class="ml-3"><b><i>Upstream Emissions in Kerosene Production</i></b></p>
<p class="ml-3"><b>Extraction</b></p>
<p class="m-3">
The extraction of crude oil, the primary feedstock for kerosene, is a significant source of upstream emissions. These emissions vary depending on the extraction method and location:
<li class="ml-[80px]"><b>Conventional Oil Extraction:</b>Conventional extraction methods emit approximately 10-20 kg CO2e per barrel of oil equivalent (BOE). This includes emissions from drilling, pumping, and initial processing stages. For instance, the energy required for these processes, along with the associated methane leaks and gas flaring, contributes to the overall carbon footprint. [Ref : R1, R2]</li>
<li class="ml-[80px]"><b>Unconventional Oil Extraction: </b> Methods such as oil sands extraction are much more carbon-intensive, emitting approximately 70-100 kg CO2e per BOE. This higher intensity is due to the additional energy required to extract and process the heavier crude. For example, oil sands extraction involves heating and treating the sands to separate the bitumen, which is an energy-intensive process.[Ref :R2, R3]</li>
</p>


<p class="m-3"><b><i>Refining</i></b></p>
<p class="m-3">
Refining crude oil into kerosene is another major source of upstream emissions. This process involves several stages, including distillation, cracking, and treatment, all of which require significant energy inputs:
<li class="ml-[80px]"><b>Energy Intensity:</b>On average, refining accounts for 7-15% of the total lifecycle emissions of kerosene. The global average emissions intensity for refining is approximately 35.3 g CO2e per MJ of kerosene produced. This includes emissions from the combustion of fossil fuels to generate the necessary heat and power for the refining processes.[Ref :R1,  R3]</li>
<li class="ml-[80px]"><b>Hydrogen Production: </b> A significant portion of the emissions from refining comes from the production of hydrogen, which is used to remove sulfur and other impurities from the crude oil. Hydrogen production is typically done through steam methane reforming, a process that is highly carbon-intensive.[Ref : R1]</li>
</p>

<p class="m-3"><b><i>Transportation</i></b></p>
<p class="m-3">
Transporting crude oil and refined kerosene also contributes to upstream emissions, though to a lesser extent compared to extraction and refining:
<li class="ml-[80px]"><b>Pipeline Transport:</b>Emissions from pipeline transport are about 1-2 g CO2e per MJ of fuel transported. This includes the energy required to pump the oil through pipelines, which can span thousands of kilometers.[Ref : R1]</li>
<li class="ml-[80px]"><b>Tanker Ship Transport: </b> Emissions from tanker ships are slightly higher, at approximately 2-3 g CO2e per MJ of fuel transported. This includes emissions from the fuel burned by the ships' engines as well as the energy used for loading and unloading the oil.[Ref :R1,  R3]</li>
</p>

<p class="m-3"><b><i>Downstream Emissions</i></b></p>
<p class="ml-3"><b><i>Combustion</i></b></p>
<p class="m-3">
The combustion of kerosene in aircraft engines is the largest source of emissions in the fuel's lifecycle:
<li class="ml-[80px]"><b>CO2 Emissions:</b>Kerosene combustion emits approximately 3.16 kg CO2 per kg of fuel burned. For a typical long-haul flight, combustion accounts for about 80-90% of the total lifecycle emissions of the fuel used. This high proportion underscores the importance of addressing combustion emissions in any decarbonization strategy.[Ref : R3]</li>
</p>

<p class="m-3"><b><i>Non-CO2 Effects</i></b></p>
<p class="ml-3">In addition to CO2, the combustion of kerosene at high altitudes produces other climate-forcing effects:
<li class="ml-[80px]"><b>Water Vapor and Contrails:</b>Water vapor emitted during combustion can lead to the formation of contrails and induced cirrus clouds, which have a warming effect. These non-CO2 effects are estimated to have a warming impact 2-4 times greater than that of CO2 alone, although there is significant uncertainty in these estimates.</li>
<li class="ml-[80px]"><b>Nitrogen Oxides (NOx): </b> NOx emissions contribute to the formation of ozone, a potent greenhouse gas, and can also lead to the destruction of methane in the atmosphere. Both of these effects further complicate the climate impact of aviation emissions.</li>
</p>

<p class="m-3"><b>Definition of eligible Carbon sink credits</b></p>
<p class="m-3">
Eligible carbon sink credits for X-SAF certification are defined as verifiable and measurable increases in carbon storage or removal from the atmosphere, achieved through approved technological or natural processes. These credits must adhere to the following criteria:
<li class="ml-[80px]"><b>Additionality: </b>The carbon removal must be additional to what would have occurred in the absence of the project.</li>
<li class="ml-[80px]"><b>Permanence: </b>The carbon must be sequestered for a significant period, typically measured in centuries.</li>
<li class="ml-[80px]"><b>Measurability:</b>The amount of carbon removed must be quantifiable using scientifically sound methodologies.</li>
<li class="ml-[80px]"><b>Verifiability: </b>The removal process and results must be independently verifiable.</li>
<li class="ml-[80px]"><b>Sustainability:</b>The removal method must not cause significant harm to ecosystems or communities.</li>
</p>


<p class="m-3"><b><i>Eligible carbon sink technologies for X-SAF certification include:</i></b></p>
<p>
<li class="ml-[80px]"><b>Direct Air Capture (DAC):</b> Technologies that extract CO2 directly from the ambient air, with subsequent permanent storage.</li>
<li class="ml-[80px]"><b>Biochar:</b>Production and application of biochar to soils, enhancing long-term carbon storage.</li>
<li class="ml-[80px]"><b>Enhanced Rock Weathering (ERW):</b>Acceleration of natural weathering processes to capture CO2 in mineral form.</li>
</p>


<p class="m-3"><b><i>These technologies must demonstrate:</i></b></p>
<p class="m-3">
In addition to CO2, the combustion of kerosene at high altitudes produces other climate-forcing effects:
<li class="ml-[80px]">Rigorous life cycle assessment, accounting for all emissions associated with the removal process.</li>
<li class="ml-[80px]">Clear traceability and monitoring of carbon removal and storage.</li>
<li class="ml-[80px]">Compliance with relevant environmental and social safeguards.</li>
<li class="ml-[80px]">Long-term storage plans to ensure permanence of carbon removal.</li>
<li class="ml-[80px]">Pathway to regulatory approval.</li>
</p>
<p class="m-3">
X-SAF certification requires that credits from these sinks be used to offset the full lifecycle emissions of aviation fuel, including both upstream and downstream processes. This comprehensive approach ensures that the aviation industry achieves true carbon neutrality while also supporting the development and scaling of carbon removal technologies.
</p>


